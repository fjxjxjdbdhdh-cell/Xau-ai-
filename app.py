    if действие in ("win", "loss"):
        результат_га = None
        with блокировка:
            сделки = загрузить_сделки()
            цель = next((t for t in сделки if t.get("id") == значение), None)
            if not цель:
                ответить_на_колбэк(колбэк_id, "Сделка не найдена")
                return
