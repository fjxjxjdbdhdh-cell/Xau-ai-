        ответить_на_колбэк(колбэк_id, "Введите: /sell ЦЕНА RSI ТРЕНД ATR")
        отправить_сообщение("Введите команду:\n`/sell ЦЕНА RSI ТРЕНД ATR`\nПример: `/sell 2400 46 DOWN 10`", чат_id=чат_id)
